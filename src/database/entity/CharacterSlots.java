package com.sample;


public class CharacterSlots {

  private long id;
  private long accid;
  private long worldid;
  private long charslots;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getAccid() {
    return accid;
  }

  public void setAccid(long accid) {
    this.accid = accid;
  }


  public long getWorldid() {
    return worldid;
  }

  public void setWorldid(long worldid) {
    this.worldid = worldid;
  }


  public long getCharslots() {
    return charslots;
  }

  public void setCharslots(long charslots) {
    this.charslots = charslots;
  }

}
