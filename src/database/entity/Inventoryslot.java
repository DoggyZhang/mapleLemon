package com.sample;


public class Inventoryslot {

  private long id;
  private long characterid;
  private long equip;
  private long use;
  private long setup;
  private long etc;
  private long cash;
  private long elab;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getCharacterid() {
    return characterid;
  }

  public void setCharacterid(long characterid) {
    this.characterid = characterid;
  }


  public long getEquip() {
    return equip;
  }

  public void setEquip(long equip) {
    this.equip = equip;
  }


  public long getUse() {
    return use;
  }

  public void setUse(long use) {
    this.use = use;
  }


  public long getSetup() {
    return setup;
  }

  public void setSetup(long setup) {
    this.setup = setup;
  }


  public long getEtc() {
    return etc;
  }

  public void setEtc(long etc) {
    this.etc = etc;
  }


  public long getCash() {
    return cash;
  }

  public void setCash(long cash) {
    this.cash = cash;
  }


  public long getElab() {
    return elab;
  }

  public void setElab(long elab) {
    this.elab = elab;
  }

}
