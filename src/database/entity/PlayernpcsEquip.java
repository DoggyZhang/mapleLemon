package com.sample;


public class PlayernpcsEquip {

  private long id;
  private long npcid;
  private long equipid;
  private long equippos;
  private long charid;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getNpcid() {
    return npcid;
  }

  public void setNpcid(long npcid) {
    this.npcid = npcid;
  }


  public long getEquipid() {
    return equipid;
  }

  public void setEquipid(long equipid) {
    this.equipid = equipid;
  }


  public long getEquippos() {
    return equippos;
  }

  public void setEquippos(long equippos) {
    this.equippos = equippos;
  }


  public long getCharid() {
    return charid;
  }

  public void setCharid(long charid) {
    this.charid = charid;
  }

}
