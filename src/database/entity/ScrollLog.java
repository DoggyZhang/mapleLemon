package com.sample;


public class ScrollLog {

  private long id;
  private long accId;
  private long chrId;
  private long scrollId;
  private long itemId;
  private long oldSlots;
  private long newSlots;
  private long hammer;
  private String result;
  private long whiteScroll;
  private long legendarySpirit;
  private long vegaId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getAccId() {
    return accId;
  }

  public void setAccId(long accId) {
    this.accId = accId;
  }


  public long getChrId() {
    return chrId;
  }

  public void setChrId(long chrId) {
    this.chrId = chrId;
  }


  public long getScrollId() {
    return scrollId;
  }

  public void setScrollId(long scrollId) {
    this.scrollId = scrollId;
  }


  public long getItemId() {
    return itemId;
  }

  public void setItemId(long itemId) {
    this.itemId = itemId;
  }


  public long getOldSlots() {
    return oldSlots;
  }

  public void setOldSlots(long oldSlots) {
    this.oldSlots = oldSlots;
  }


  public long getNewSlots() {
    return newSlots;
  }

  public void setNewSlots(long newSlots) {
    this.newSlots = newSlots;
  }


  public long getHammer() {
    return hammer;
  }

  public void setHammer(long hammer) {
    this.hammer = hammer;
  }


  public String getResult() {
    return result;
  }

  public void setResult(String result) {
    this.result = result;
  }


  public long getWhiteScroll() {
    return whiteScroll;
  }

  public void setWhiteScroll(long whiteScroll) {
    this.whiteScroll = whiteScroll;
  }


  public long getLegendarySpirit() {
    return legendarySpirit;
  }

  public void setLegendarySpirit(long legendarySpirit) {
    this.legendarySpirit = legendarySpirit;
  }


  public long getVegaId() {
    return vegaId;
  }

  public void setVegaId(long vegaId) {
    this.vegaId = vegaId;
  }

}
