package com.sample;


public class DropDataGlobal {

  private long id;
  private long continent;
  private long dropType;
  private long itemid;
  private long minimumQuantity;
  private long maximumQuantity;
  private long questid;
  private long chance;
  private String comments;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getContinent() {
    return continent;
  }

  public void setContinent(long continent) {
    this.continent = continent;
  }


  public long getDropType() {
    return dropType;
  }

  public void setDropType(long dropType) {
    this.dropType = dropType;
  }


  public long getItemid() {
    return itemid;
  }

  public void setItemid(long itemid) {
    this.itemid = itemid;
  }


  public long getMinimumQuantity() {
    return minimumQuantity;
  }

  public void setMinimumQuantity(long minimumQuantity) {
    this.minimumQuantity = minimumQuantity;
  }


  public long getMaximumQuantity() {
    return maximumQuantity;
  }

  public void setMaximumQuantity(long maximumQuantity) {
    this.maximumQuantity = maximumQuantity;
  }


  public long getQuestid() {
    return questid;
  }

  public void setQuestid(long questid) {
    this.questid = questid;
  }


  public long getChance() {
    return chance;
  }

  public void setChance(long chance) {
    this.chance = chance;
  }


  public String getComments() {
    return comments;
  }

  public void setComments(String comments) {
    this.comments = comments;
  }

}
