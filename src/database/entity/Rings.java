package com.sample;


public class Rings {

  private long ringid;
  private long partnerRingId;
  private long partnerChrId;
  private long itemid;
  private String partnername;


  public long getRingid() {
    return ringid;
  }

  public void setRingid(long ringid) {
    this.ringid = ringid;
  }


  public long getPartnerRingId() {
    return partnerRingId;
  }

  public void setPartnerRingId(long partnerRingId) {
    this.partnerRingId = partnerRingId;
  }


  public long getPartnerChrId() {
    return partnerChrId;
  }

  public void setPartnerChrId(long partnerChrId) {
    this.partnerChrId = partnerChrId;
  }


  public long getItemid() {
    return itemid;
  }

  public void setItemid(long itemid) {
    this.itemid = itemid;
  }


  public String getPartnername() {
    return partnername;
  }

  public void setPartnername(String partnername) {
    this.partnername = partnername;
  }

}
